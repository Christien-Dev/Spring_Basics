package com.example.hibernatejpa.dao;

import com.example.hibernatejpa.domains.Student;
import jakarta.persistence.EntityManager;
import jakarta.persistence.Query;
import jakarta.persistence.TypedQuery;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Repository;
import org.springframework.transaction.annotation.Transactional;

import java.util.List;

@Repository
public class StudentDAOImpl implements StudentDAO{

    private EntityManager entityManager;

    @Autowired
    public StudentDAOImpl(EntityManager entityManager) {
        this.entityManager = entityManager;
    }

    @Override
    @Transactional
    public void save(Student student) {
        entityManager.persist(student);
    }

    @Override
    public Student findById(int id) {
        return entityManager.find(Student.class, id);
    }

    @Override
    public List<Student> findAll() {
        TypedQuery<Student> query = entityManager.createQuery("FROM Student", Student.class);
        return query.getResultList();
    }

    @Override
    public List<Student> findAllSorted() {
        TypedQuery<Student> query = entityManager.createQuery("FROM Student order by lastName asc ", Student.class);
        return query.getResultList();
    }

    @Override
    public List<Student> findByLastName(String lastName) {
        TypedQuery<Student> query = entityManager.createQuery("FROM Student where lastName=:lastName", Student.class);
        query.setParameter("lastName", lastName);
        return query.getResultList();
    }

    @Override
    @Transactional
    public void update(Student student) {
        entityManager.merge(student);
    }

    @Override
    @Transactional
    public int updateAllStudents(String lastName) {
        Query query = entityManager.createQuery("UPDATE Student SET lastName=:lastName");
        query.setParameter("lastName", lastName);
        return query.executeUpdate();
    }

    @Override
    @Transactional
    public void deleteStudents(int studentId) {
        Student student = entityManager.find(Student.class, studentId);
        entityManager.remove(student);
    }

    @Override
    @Transactional
    public int deleteStudentsByFirstName(String firstName) {
        Query query = entityManager.createQuery("DELETE FROM Student WHERE firstname=:firstName");
        query.setParameter("firstName", firstName);
        return query.executeUpdate();
    }
}
